import SwiftUI

extension Font {
    
    static func montserratRegular(size: CGFloat) -> Font {
        Font.custom("Montserrat-Regular", size: size)
    }
    
    static func playfairDisplayRegular(size: CGFloat) -> Font {
        Font.custom("PlayfairDisplay-Regular", size: size)
    }
}
